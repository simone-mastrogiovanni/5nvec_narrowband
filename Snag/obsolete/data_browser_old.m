   D_B.data.chname=' ';
   D_B.data.chnumber=0;
   D_B.filter.n=0;
   D_B.filter.capt='no filters';
   D_B.proc.type='tfpows';
   D_B.proc.iter=20;
   
   eval(dataString);
end

