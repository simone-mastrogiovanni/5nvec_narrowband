dataString=[...
'[ntype,file]=fr_fildatsel;' ...
'D_B.data.type=ntype;' ...
'D_B.data.file=file;' ...
...
'[chn,chs,dt,dlen,sp]=fr_selch(ntype,file);' ...
'D_B.data.chname=chs;' ...
'D_B.data.chnumber=chn;' ...
'D_B.data.dt=dt;' ...
'D_B.data.dlen=dlen;' ...
'D_B.data.sp=sp;' ...
'if ntype == 100; D_B.data.file=chs; end;'];

appo=who('D_B');
if length(appo) <= 0
   D_B.access='by file';
   D_B.data.type=100;
   D_B.data.file='nofile';
